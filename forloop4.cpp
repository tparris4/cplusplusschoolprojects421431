#include <iostream>
using namespace std;

int main()
{
	int x;
	int y;
	cout << "Enter how many stars you want in the first row \n";
	cin >> x;
	for (int i = 1; i <= x; i++){
		for (int j = 1; j <= x; j++){
		
			cout << "*";
		}
		x--;
		cout << "\n";
}
cout << "*";
cin >> y;
}
